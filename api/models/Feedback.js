const mongoose = require("mongoose");


const Feedback = new mongoose.Schema({
  name: {
    type: String,
  },
  email: {
    type: String,
  },
  subject: {
    type: String,
  },
  message: {
    type: String,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
}, {collection: "Feedback"});

module.exports = mongoose.model("Feedback", Feedback);
